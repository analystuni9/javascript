let n1 = 10; // Inicializa a variável n1 com o valor 10
let n2 = 20; // Inicializa a variável n2 com o valor 20
let n3 = 30; // Inicializa a variável n3 com o valor 30

console.log(n1, n2, n3); // Imprime os valores das variáveis n1, n2 e n3

console.log(n1 == "10"); // Verifica se n1 é igual a "10" em valor (true)
console.log(n1 === "10"); // Verifica se n1 é igual a "10" em valor e tipo (false)

console.log(n2 < 10); // Verifica se n2 é menor que 10 (false)
console.log(n1 >= 11); // Verifica se n1 é maior ou igual a 11 (false)
console.log(n1 <= 11); // Verifica se n1 é menor ou igual a 11 (true)

console.log(n1 != "10"); // Verifica se n1 é diferente de "10" em valor (false)
console.log(n1 !== "10"); // Verifica se n1 é diferente de "10" em valor e tipo (true)

// Esses operadores de comparação são usados para comparar os valores de variáveis ou expressões. O resultado de cada operação é um valor booleano (true ou false), dependendo se a condição é verdadeira ou falsa. O operador == verifica a igualdade de valor, enquanto === verifica igualdade de valor e tipo. Os operadores <, >, <=, e >= comparam os valores, e != e !== verificam a diferença em valor e/ou tipo.