let i = 0; // Inicializa a variável i com o valor 0

// Decrementa o valor de i antes de ser avaliado e, em seguida, imprime o valor atual de i, que é -1
console.log(--i);

// Imprime o valor atual de i e depois decrementa i. Nesse momento, i torna-se -2.
console.log(i--);

// Imprime o valor atual de i, que é -2
console.log(i);

