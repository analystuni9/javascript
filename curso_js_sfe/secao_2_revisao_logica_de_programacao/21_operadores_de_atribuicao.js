// Inicializando duas variáveis n1 e n2 com os valores 10 e 5, respectivamente
let n1 = 10;
let n2 = 5;

// Adicionando n1 e n2 e imprimindo o resultado
console.log(n1 + n2); // Resultado: 15

// Subtraindo n2 de n1 e imprimindo o resultado
console.log(n1 - n2); // Resultado: 5

// Multiplicando n1 por n2 e imprimindo o resultado
console.log(n1 * n2); // Resultado: 50

// Dividindo n1 por n2 e imprimindo o resultado
console.log(n1 / n2); // Resultado: 2

// Calculando o resto da divisão de n1 por n2 (módulo) e imprimindo o resultado
console.log(n1 % n2); // Resultado: 0 (pois 10 dividido por 5 não deixa resto)

// Calculando n1 elevado à potência de n2 e imprimindo o resultado
console.log(2 ** 3); // Resultado: 8 (2 elevado à 3ª potência)
