// Definição de uma função tradicional chamada 'hello'
function hello() {
    // Mensagem de console indicando que é uma função tradicional
    console.log('traditional function');
    // Mensagem de console exibindo 'Hello World!'
    console.log('Hello World!');
}

// Chamada da função 'hello'
hello();

// Definição de uma expressão de função atribuída à constante 'welcome'
const welcome = function() {
    // Mensagem de console indicando que é uma expressão de função
    console.log('welcome to the world of programming');
    // Mensagem de console exibindo 'function expression'
    console.log('function expression');
}

// Chamada da função expressão 'welcome'
welcome();

// Definição de uma arrow function (função de seta) atribuída à variável 'congratulations'
let congratulations = () => {
    // Mensagem de console indicando que é uma arrow function
    console.log('Congratulations, keep studying');
    // Mensagem de console exibindo 'arrow function'
    console.log('arrow function');
}

// Chamada da arrow function 'congratulations'
congratulations();
