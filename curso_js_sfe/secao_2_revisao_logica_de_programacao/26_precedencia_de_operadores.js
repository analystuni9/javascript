let n1 = 6; // Primeiro número
let n2 = 8; // Segundo número

// Multiplicação e divisão têm preferência na execução dos cálculos.
// Aqui estamos calculando a média entre n1 e n2.
let media = (n1 + n2) / 2; // Calcula a média dos dois números

// Imprime a média no console
console.log(`${media}`);

// Aqui estamos calculando (3 * 2) ** 2.
// Primeiro, multiplicamos 3 por 2, resultando em 6.
// Em seguida, elevamos 6 ao quadrado, resultando em 36.
console.log((3 * 2) ** 2); // Imprime o resultado (36)
